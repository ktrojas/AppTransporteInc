package com.java.ws.rest.vo;

public class InfoContenedores {
	
	
    private String nombreContenedor;
    private int costo;
    private int kpi;
    private int kpiNodespachado;
    
	public String getNombreContenedor() {
		return nombreContenedor;
	}
	public void setNombreContenedor(String nombreContenedor) {
		this.nombreContenedor = nombreContenedor;
	}
	public int getCosto() {
		return costo;
	}
	public void setCosto(int costo) {
		this.costo = costo;
	}
	public int getKpi() {
		return kpi;
	}
	public void setKpi(int kpi) {
		this.kpi = kpi;
	}
	public int getKpiNodespachado() {
		return kpiNodespachado;
	}
	public void setKpiNodespachado(int kpiNodespachado) {
		this.kpiNodespachado = kpiNodespachado;
	}

    
    
}